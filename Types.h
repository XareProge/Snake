#pragma once
#include <string>

// ─── Размеры окна и поля ─────────────────────────────────────────
constexpr int CELL  = 26;            // размер одной клетки в пикселях
constexpr int COLS  = 28;            // столбцов на поле
constexpr int ROWS  = 22;            // строк на поле
constexpr int UI_W  = 220;           // ширина правой панели
constexpr int WIN_W = COLS * CELL + UI_W;
constexpr int WIN_H = ROWS * CELL;

// ─── Состояние игры ──────────────────────────────────────────────
enum class State {
    MENU,         // главное меню
    DIFFICULTY,   // выбор сложности
    PLAYING,      // игровой процесс
    GAME_OVER,    // конец игры
    RECORDS       // таблица рекордов
};

// ─── Направление движения ────────────────────────────────────────
enum class Dir { UP, DOWN, LEFT, RIGHT };

// ─── Уровень сложности ───────────────────────────────────────────
enum class Diff { HARMLESS = 0, EASY, MEDIUM, HARD };

// ─── Тип клетки на игровом поле ──────────────────────────────────
enum class Cell {
    EMPTY,      // пусто
    FOOD,       // обычная еда     (+10 очков)
    POISON,     // ядовитая еда    (-15 очков, змейка укорачивается)
    WALL,       // стена           (конец игры)
    TELEPORT,   // телепорт        (перемещает змейку случайно)
    INVERT      // инверсия        (управление переворачивается на 30 ходов)
};

// ─── Точка на сетке ──────────────────────────────────────────────
struct Pt {
    int x = 0, y = 0;
    bool operator==(const Pt& o) const { return x == o.x && y == o.y; }
};

// ─── Запись рекорда ──────────────────────────────────────────────
struct Record {
    std::string date;        // дата и время в формате дд.мм.гггг чч:мм
    std::string diff;        // название сложности
    int         score = 0;
    bool        isAI  = false; // true = рекорд установлен ИИ
};

// ─── Вспомогательные функции ─────────────────────────────────────
std::string diffName(Diff d);    // название сложности на русском
std::string currentDate();       // текущая дата и время
